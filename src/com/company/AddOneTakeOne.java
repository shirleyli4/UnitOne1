package com.company;
import java.util.Scanner;
public class AddOneTakeOne {
    public static void main (String[] args){
        Scanner input = new Scanner(System.in);
        System.out.println("Enter a number: ");
        int num = input.nextInt();
        num--;
        System.out.print(num +" ");
        num++;
        System.out.print(num +" ");
        num++;
        System.out.print(num +" ");



    }
}
